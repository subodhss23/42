int		ft_compact(char **tab, int lenght)
{
	int i;
	int j;
	char **tmp;

	i = 0;
	j = 0;
	tmp = 0;
	while (i < lenght)
	{
		if (tab[i] != 0) 
		{
			tmp[j] = tab[i];
			i++;
			j++;
		}
		i++;
	}
	tab = tmp;
	return (0);
}
